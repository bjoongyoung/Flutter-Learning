//GestureDetector widget
//제스처 위젯

/onTap
//한 번 탭했을 때 실행되는 함수를 입력할 수 있다.

/onDoubleTap
//두 번 연속으로 탭했을 떄 실행되는 함수를 입력할 수 있다.

/onLongPress
//길게 누르기가 인식되었을 때 실행되는 함수르 입력할 수 있다.

/onPanStart
//수평 또는 수직으로 드래그가 시작되었을 떄 실행되는 함수를 입력할 수 있다.

/onPanUpdate
//수평 또는 수직으로 드래그를 하는 동안 업데이트될 때마다 실행되는 함수를 입력할 수 있다.

/onPanEnd
//수평 또는 수직으로 드래그가 끝났을 때 실행되는 함수를 입력할 수 있다.

/onHorizontalDragStart
//수평으로 드래그를 시작했을 때 실행되는 함수를 입력할 수 있다.

/onHorizontalDragUpdate
//수평으로 드래그를 하는 동안 드래그 위치가 업데이트될 때마다 실행되는 함수를 입력할 수 있다.

/onHorizontalDragEnd
//수평으로 드래그가 끝났을 떄 실행되는 함수를 입력할 수 있다.

/onVerticalDragStart
//수직으로 드래그를 시작했을 떄 실행되는 함수를 입력할 수 있다.

/onVerticalDragUpdate
//수직으로 드래그를 하는 동안 드래그 위치가 업데이트될 때마다 실행되는 함수를 입력할 수 있다.
  
/onVerticalDragEnd
//수직으로 드래그가 끝났을 때 진행되는 함수를 입력할 수 있다.

/onScaleDragStart
//확대가 시작되었을 때 실행되는 함수를 입력할 수 있다.

/onScaleDragUpdate
//확대가 진행되는 동안 확대가 업데이트 될 때마다 실행되는 함수를 입력할 수 있다.

/onScaleDragEnd
//확대가 끝났을 떄 살행되는 함수를 입력할 수 있다.
